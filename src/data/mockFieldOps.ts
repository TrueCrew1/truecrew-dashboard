const now = new Date();
const today = new Date(now.getFullYear(), now.getMonth(), now.getDate());
const isoAt = (hoursFromMidnight: number, dayOffset = 0) =>
  new Date(today.getTime() + dayOffset * 86400000 + hoursFromMidnight * 3600000).toISOString();
const isoPast = (hoursAgo: number) =>
  new Date(now.getTime() - hoursAgo * 3600000).toISOString();
const isoFuture = (hoursAhead: number) =>
  new Date(now.getTime() + hoursAhead * 3600000).toISOString();

